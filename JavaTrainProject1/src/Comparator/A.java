package Comparator;

class A{
	int x;
	public A print() {
		System.out.println("Next Iteration is from print() : " +  x);
		return new A(8);
	}
	public A forward() {
		System.out.println("Next Iteration is from forward(): " +  x);
		return new A(5);
	}
	public void last() {
		System.out.println("last Iteration : " +  x);
	}
	public A(int a) {x=a;}
	public A() {}
}


